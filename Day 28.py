# f strings in Python.

name = "Himanshu Soni"
country = "India"

# To print variables in between string we use f string like this.
print(f"Hi my name is {name} and my country is {country}")

# To print string as it is, not replacing variable still we can use f string like this.
print(f"Hi my name is {{name}} and my country is {{country}}")
