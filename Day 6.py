# Data Types and Variables.

# Variables hold data.

a = 1
print(a)  # Number can be int, float or complex
print(complex(2, 3))

b = True
print(b)  # Boolean

c = "Himanshu"
print(c)  # String

d = None
print(d)  # None

# Type Checking.
print("The type of a,b,c,d are", type(a), type(b), type(c), type(d))

