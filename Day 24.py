# Tuples in Python.

# Most basic difference between tuple and list is that List can be modified and tuples can not be modified and changed.
tup1 = (1, 2, 3, 4, 5, 6, 7, "Himanshu")

# If we are creating a tuple with a single element, we need to add a comma at the end. It is mandatory.
tup2 = (1,)

# Rest almost every concept is same like List. For example, Range, slicing, negative indexes etc.

print(tup1)
